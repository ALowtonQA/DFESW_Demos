package com.qa.operators;

public class Operators {

	public void basicMaths(int numOne, int numTwo) {
		// Maths operators
		System.out.println(numOne + numTwo);
		System.out.println(numOne - numTwo);
		System.out.println(numOne * numTwo);
		System.out.println(numOne / numTwo);
	}
	
	public void modulus(int numOne, int numTwo) {
		System.out.println(numOne % numTwo);
	}
}
